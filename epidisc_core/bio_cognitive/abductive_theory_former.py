class HypothesisType(Enum):
    """Types of scientific hypotheses"""
    CAUSAL = "causal"  # X causes Y
    MECHANISTIC = "mechanistic"  # X works through mechanism Y
    COMPARATIVE = "comparative"  # X differs from Y in aspect Z
    TEMPORAL = "temporal"  # X precedes/influences Y over time
    INTERVENTION = "intervention"  # Manipulating X affects Y
    INTEGRATIVE = "integrative"  # Integrates multiple phenomena


class ConfidenceLevel(Enum):
    """Confidence levels for hypotheses"""
    VERY_LOW = 1
    LOW = 2
    MODERATE = 3
    HIGH = 4
    VERY_HIGH = 5


@dataclass
class Hypothesis:
    """
    A scientific hypothesis generated by abductive reasoning

    Attributes:
        statement: The hypothesis statement
        hypothesis_type: Type of hypothesis
        confidence: Confidence level (1-5)
        evidence: Supporting evidence
        predictions: Testable predictions
        falsifiable: Whether hypothesis is falsifiable
        novelty: Novelty score (0-1)
        plausibility: Plausibility score (0-1)
        source_domains: Which domains contributed to this hypothesis
        mechanisms: Proposed mechanisms (if mechanistic hypothesis)
        knowledge_gaps: Related knowledge gaps
    """
    statement: str
    hypothesis_type: HypothesisType
    confidence: ConfidenceLevel
    evidence: List[str] = field(default_factory=list)
    predictions: List[str] = field(default_factory=list)
    falsifiable: bool = True
    novelty: float = 0.5
    plausibility: float = 0.5
    source_domains: List[str] = field(default_factory=list)
    mechanisms: List[str] = field(default_factory=list)
    knowledge_gaps: List[str] = field(default_factory=list)

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary"""
        return {
            'statement': self.statement,
            'type': self.hypothesis_type.value,
            'confidence': self.confidence.value,
            'evidence': self.evidence,
            'predictions': self.predictions,
            'falsifiable': self.falsifiable,
            'novelty': self.novelty,
            'plausibility': self.plausibility,
            'source_domains': self.source_domains,
            'mechanisms': self.mechanisms,
            'knowledge_gaps': self.knowledge_gaps
        }


@dataclass
class KnowledgeGap:
    """
    A gap in current biological knowledge

    Attributes:
        description: Description of the knowledge gap
        domain: Which domain(s) this gap relates to
        importance: How important filling this gap is (1-5)
        fillable: Whether this gap is currently fillable with existing techniques
        related_hypotheses: Hypotheses that could fill this gap
    """
    description: str
    domain: str
    importance: int = 3
    fillable: bool = True
    related_hypotheses: List[str] = field(default_factory=list)

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary"""
        return {
            'description': self.description,
            'domain': self.domain,
            'importance': self.importance,
            'fillable': self.fillable,
            'related_hypotheses': self.related_hypotheses
        }


@dataclass
class Theory:
    """
    A synthesized scientific theory combining multiple hypotheses

    Attributes:
        name: Name of the theory
        description: Description of the theory
        hypotheses: Constituent hypotheses
        coherence: How coherent the theory is (0-1)
        explanatory_power: How much the theory explains (0-1)
        predictions: Testable predictions
        scope: Scope of the theory
    """
    name: str
    description: str
    hypotheses: List[Hypothesis] = field(default_factory=list)
    coherence: float = 0.5
    explanatory_power: float = 0.5
    predictions: List[str] = field(default_factory=list)
    scope: str = "specific"

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary"""
        return {
            'name': self.name,
            'description': self.description,
            'hypotheses': [h.to_dict() for h in self.hypotheses],
            'coherence': self.coherence,
            'explanatory_power': self.explanatory_power,
            'predictions': self.predictions,
            'scope': self.scope
        }


